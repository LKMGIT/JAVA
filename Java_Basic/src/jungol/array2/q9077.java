package jungol.array2;

public class q9077 {
    public static void main(String[] args) {
        int[][] arr = new int[][]{{3,5,4}, {2,6,7}, {8,10,1}};

        for(int i = 0; i< arr.length; i ++){
            for(int j = 0; j < arr.length; j ++){
                System.out.printf("%d ", arr[i][j]);
            }
            System.out.println("");
        }


    }
}
